    <strong>Customer Key:</strong> 
    The customer key is the unique key assigned to you from <a href="http://www.timitek.com" target="_blank">www.timitek.com</a>.
    <br /><br />
    
    <strong>Cache Settings:</strong> 
    By default, GetRETS will pre-fetch and cache all of your listings from the MLS systems.  
    It will maintain this cache for you automatically and allow you to fetch your listing details even if your MLS system is offline.<br />
    It is not recommended that you disable this, however, you can optionally disable the cache and have all of your listings fetched
    directly from the MLS system on each request.  Some MLS systems do not allow this sort of traffic, and you must be sure this is what
    you want to do before checking this box.
    <br /><br />
    
    <strong>Show Thumbnail:</strong> 
    Some WordPress templates will show a featured image tied to a post.  When a template with this feature enabled is used, having
    GetRETS also display a thumbnail, might be a bit redundant.  By unchecking this box GetRETS will not present a thumbnail associated
    with the listing on the search results.
